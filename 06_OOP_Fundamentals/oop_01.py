# Object-Oriented Programming Fundamental.

# A simple class Book.
class Book:
    pass

# Creating the instances/objects of the class Book.
b1 = Book()
b2 = Book()

# Displaying book objects.
print(f'\nBook1 - {b1}')
print(f'\nBook2 - {b2}')

# Check if they are the same object.
print(f'\nAre b1 and b2 the same object? {b1 is b2}')
